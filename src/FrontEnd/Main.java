    private boolean IsSelected(JComboBox ComboBox, String Campo) {
        if (ComboBox.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, 
                        "Selecionar uma opção do " + Campo, 
                        "Validação", 
                        JOptionPane.ERROR_MESSAGE);
            ComboBox.requestFocus();
            return false;
        }
        else
            return true;
    }   
    
    private boolean IsFloat(JTextField TextField, String Campo) {
        boolean validar;
        String msg = "";
        try {
            if (TextField.getText().isEmpty()) {
                msg = "Preencher o campo: " ;
                validar = false;
            }
            else {
                float f = Float.parseFloat(TextField.getText());
                validar = true;
            }
        }
        catch(Exception e) {
            msg = "Preencher um valor numerico para o campo: ";
            validar = false;
        }
        
        if (!validar) {
            JOptionPane.showMessageDialog(this, 
                        msg + Campo, 
                        "Validação", 
                        JOptionPane.ERROR_MESSAGE);
            TextField.setText("");
            TextField.requestFocus();        
        }
        
        return validar;          
    }

