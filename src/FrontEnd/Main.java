    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        if (IsFloat(txtConsumoKwh, "Consumo kw/h")
                && IsSelected(cbBandeira, "Bandeira")) {                               

                float c = Float.parseFloat(txtConsumoKwh.getText());
                
                //processamento
                float vkwh, tb;
                if (cbBandeira.getSelectedIndex() == 0) {
                    vkwh = 0.736f;
                    tb = 0;
                }
                else if (cbBandeira.getSelectedIndex() == 1) {
                    vkwh = 0.817f;
                    tb = 4.78f;
                }
                else {
                    vkwh = 0.875f;
                    tb = 7.53f;
                }

                float cm = c * vkwh;
                float ti = 31.75f + 0.575f/100.0f * cm;
                float vf =  cm + ti + tb;
                
                lblConsumoMensalSaida.setText("R$ " + String.format("%.2f", cm));
                lblTaxaBandieraSaida.setText("R$ " + String.format("%.2f", tb));
                lblTaxaIluminacaoSaida.setText("R$ " + String.format("%.2f", ti));
                lblValorTotalSaida.setText("R$ " + String.format("%.2f", vf));                
        }       
        else {
            lblConsumoMensalSaida.setText("");
            lblTaxaBandieraSaida.setText("");
            lblTaxaIluminacaoSaida.setText("");
            lblValorTotalSaida.setText("");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

